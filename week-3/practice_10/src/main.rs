fn main() {
    // addition
    let sum = 5_550 + 7_310;
    println!("The sum of 5_550 and 7_310 is {}",sum);

    // substraction
    let difference:f32 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 = {}",difference);

    // multiplication
    let product:f64 = 4.0 * 30.0;
    println!("The multiple of 4.0 and 30.0 is{}",product);

    // division
    let quotient = 56.7/ 32.2;
    println!("The division of 56.7 and 32.2 is {}",quotient);

    // remainder
    let remainder = 43.0 % 5.0;
    println!("The remainder of 43.0 and 5.0 = {}", remainder);
}
